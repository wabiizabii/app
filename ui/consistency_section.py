import streamlit as st
import math

def render_consistency_section():
    """
    ‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏• Section ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡πÄ‡∏Ñ‡∏£‡∏∑‡πà‡∏≠‡∏á‡∏°‡∏∑‡∏≠‡∏ß‡∏¥‡πÄ‡∏Ñ‡∏£‡∏≤‡∏∞‡∏´‡πå Profit Consistency
    *** ‡∏≠‡∏±‡∏õ‡πÄ‡∏Å‡∏£‡∏î: ‡∏™‡∏≤‡∏°‡∏≤‡∏£‡∏ñ‡∏à‡∏±‡∏î‡∏Å‡∏≤‡∏£‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏Ç‡∏≤‡∏î‡∏ó‡∏∏‡∏ô‡πÅ‡∏•‡∏∞‡∏ß‡∏≤‡∏á‡πÅ‡∏ú‡∏ô‡πÑ‡∏î‡πâ‡∏Ñ‡∏£‡∏ö‡∏ß‡∏á‡∏à‡∏£ ***
    """
    with st.expander("üéØ Profit Consistency Analysis & Plan", expanded=True):
        
        # --- 1. ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î‡∏à‡∏≤‡∏Å Sidebar ---
        initial_balance = st.session_state.get('consistency_initial_balance', 1.0)
        profit_target_pct = st.session_state.get('consistency_profit_target_pct', 10.0)
        total_pl = st.session_state.get('consistency_total_pl', 0.0)
        consistency_percent = st.session_state.get('consistency_percent', 0.0)
        rule_threshold = st.session_state.get('consistency_rule_threshold', 30)
        daily_target = st.session_state.get('consistency_daily_target', 300.0)

        # --- 2. ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏Ñ‡πà‡∏≤‡∏û‡∏∑‡πâ‡∏ô‡∏ê‡∏≤‡∏ô ---
        challenge_profit_target_usd = initial_balance * (profit_target_pct / 100)

        # --- 3. ‡∏ß‡∏¥‡πÄ‡∏Ñ‡∏£‡∏≤‡∏∞‡∏´‡πå‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡πÅ‡∏•‡∏∞‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢ ---
        st.success("#### ‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡πÅ‡∏•‡∏∞‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏Ç‡∏≠‡∏á‡∏Ñ‡∏∏‡∏ì:")
        
        # ‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô: ‡∏Ç‡∏≤‡∏î‡∏ó‡∏∏‡∏ô
        if total_pl < 0:
            profit_to_breakeven = abs(total_pl)
            profit_to_final_target = profit_to_breakeven + challenge_profit_target_usd
            st.error(f"**‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô:** ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏Ç‡∏≤‡∏î‡∏ó‡∏∏‡∏ô‡∏™‡∏∏‡∏ó‡∏ò‡∏¥‡∏≠‡∏¢‡∏π‡πà **${total_pl:,.2f}**")
            
            col1, col2 = st.columns(2)
            col1.metric("üí∞ ‡∏ï‡πâ‡∏≠‡∏á‡∏ó‡∏≥‡∏Å‡∏≥‡πÑ‡∏£‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Ñ‡∏∑‡∏ô‡∏ó‡∏∏‡∏ô (Breakeven)", f"${profit_to_breakeven:,.2f}")
            col2.metric("üèÅ ‡∏ï‡πâ‡∏≠‡∏á‡∏ó‡∏≥‡∏Å‡∏≥‡πÑ‡∏£‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏ñ‡∏∂‡∏á‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢ 10%", f"${profit_to_final_target:,.2f}")

            st.info("üí° **‡πÅ‡∏ú‡∏ô‡∏£‡∏∞‡∏¢‡∏∞‡πÅ‡∏£‡∏Å:** ‡πÇ‡∏ü‡∏Å‡∏±‡∏™‡∏ó‡∏µ‡πà‡∏Å‡∏≤‡∏£‡πÄ‡∏ó‡∏£‡∏î‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏Å‡∏•‡∏±‡∏ö‡∏°‡∏≤‡∏ó‡∏µ‡πà‡∏à‡∏∏‡∏î‡∏Ñ‡∏∏‡πâ‡∏°‡∏ó‡∏∏‡∏ô‡∏Å‡πà‡∏≠‡∏ô ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏°‡∏µ‡∏Å‡∏≥‡πÑ‡∏£‡∏™‡∏∏‡∏ó‡∏ò‡∏¥‡πÅ‡∏•‡πâ‡∏ß ‡∏£‡∏∞‡∏ö‡∏ö‡∏à‡∏∞‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ß‡∏¥‡πÄ‡∏Ñ‡∏£‡∏≤‡∏∞‡∏´‡πå‡∏Å‡∏é Consistency ‡πÉ‡∏´‡πâ")

        # ‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô: ‡∏°‡∏µ‡∏Å‡∏≥‡πÑ‡∏£
        else:
            try:
                best_day = 0.0
                if consistency_percent > 0:
                    best_day = (consistency_percent / 100) * total_pl
                elif total_pl > 0 and consistency_percent == 0:
                    best_day = total_pl
                
                if best_day > 0:
                    consistency_target_usd = best_day / (rule_threshold / 100)
                    final_target = max(challenge_profit_target_usd, consistency_target_usd)
                    profit_needed = final_target - total_pl
                    if profit_needed < 0: profit_needed = 0

                    st.success(f"**‡∏™‡∏ñ‡∏≤‡∏ô‡∏∞‡∏õ‡∏±‡∏à‡∏à‡∏∏‡∏ö‡∏±‡∏ô:** ‡∏°‡∏µ‡∏Å‡∏≥‡πÑ‡∏£‡∏™‡∏∏‡∏ó‡∏ò‡∏¥ **+${total_pl:,.2f}**")

                    col1, col2, col3 = st.columns(3)
                    col1.metric("üèÅ ‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏Å‡∏≥‡πÑ‡∏£ Challenge (10%)", f"${challenge_profit_target_usd:,.2f}")
                    col2.metric("üéØ ‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏Å‡∏≥‡πÑ‡∏£ Consistency", f"${consistency_target_usd:,.2f}")
                    col3.metric("üèÜ ‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏™‡∏∏‡∏î‡∏ó‡πâ‡∏≤‡∏¢‡∏ó‡∏µ‡πà‡∏ï‡πâ‡∏≠‡∏á‡πÑ‡∏õ‡πÉ‡∏´‡πâ‡∏ñ‡∏∂‡∏á", f"${final_target:,.2f}", delta="‡∏™‡∏π‡∏á‡∏Å‡∏ß‡πà‡∏≤‡∏Ñ‡∏∑‡∏≠‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏à‡∏£‡∏¥‡∏á")

                    st.warning(f"**‡∏Å‡∏é‡πÄ‡∏´‡∏•‡πá‡∏Å (Speed Limit):** ‡∏´‡πâ‡∏≤‡∏°‡∏ó‡∏≥‡∏Å‡∏≥‡πÑ‡∏£‡∏™‡∏∏‡∏ó‡∏ò‡∏¥‡πÉ‡∏ô‡∏ß‡∏±‡∏ô‡πÉ‡∏î‡∏ß‡∏±‡∏ô‡∏´‡∏ô‡∏∂‡πà‡∏á‡πÄ‡∏Å‡∏¥‡∏ô **${best_day:,.2f}** ‡πÇ‡∏î‡∏¢‡πÄ‡∏î‡πá‡∏î‡∏Ç‡∏≤‡∏î!")

                    # --- ‡∏™‡πà‡∏ß‡∏ô Simulator (‡∏à‡∏∞‡∏ó‡∏≥‡∏á‡∏≤‡∏ô‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏°‡∏µ‡∏Å‡∏≥‡πÑ‡∏£‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô) ---
                    st.divider()
                    st.info("#### üìà ‡∏à‡∏≥‡∏•‡∏≠‡∏á‡∏™‡∏ñ‡∏≤‡∏ô‡∏Å‡∏≤‡∏£‡∏ì‡πå (Scenario Simulator)")

                    if daily_target > best_day:
                        st.error(f"‚ö†Ô∏è **‡∏Ñ‡∏≥‡πÄ‡∏ï‡∏∑‡∏≠‡∏ô:** ‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏Å‡∏≥‡πÑ‡∏£‡∏ï‡πà‡∏≠‡∏ß‡∏±‡∏ô (${daily_target:,.2f}) ‡∏™‡∏π‡∏á‡∏Å‡∏ß‡πà‡∏≤ Speed Limit (${best_day:,.2f}) ‡∏ã‡∏∂‡πà‡∏á‡πÄ‡∏™‡∏µ‡πà‡∏¢‡∏á‡∏ï‡πà‡∏≠‡∏Å‡∏≤‡∏£‡∏™‡∏≠‡∏ö‡∏ï‡∏Å!")
                    
                    if profit_needed > 0:
                        days_to_target = math.ceil(profit_needed / daily_target) if daily_target > 0 else 0
                        st.write(f"‡∏ñ‡πâ‡∏≤‡∏Ñ‡∏∏‡∏ì‡∏ó‡∏≥‡∏Å‡∏≥‡πÑ‡∏£‡πÄ‡∏â‡∏•‡∏µ‡πà‡∏¢‡∏ß‡∏±‡∏ô‡∏•‡∏∞ **${daily_target:,.2f}**:")
                        
                        sim_col1, sim_col2 = st.columns(2)
                        sim_col1.metric("‡∏Ñ‡∏∏‡∏ì‡∏à‡∏∞‡πÑ‡∏õ‡∏ñ‡∏∂‡∏á‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏™‡∏∏‡∏î‡∏ó‡πâ‡∏≤‡∏¢‡πÉ‡∏ô", f"~{days_to_target} ‡∏ß‡∏±‡∏ô‡∏ó‡∏≥‡∏Å‡∏≤‡∏£" if days_to_target > 0 else "N/A")
                        sim_col2.metric("‡∏¢‡∏±‡∏á‡∏ï‡πâ‡∏≠‡∏á‡∏ó‡∏≥‡∏Å‡∏≥‡πÑ‡∏£‡∏≠‡∏µ‡∏Å", f"${profit_needed:,.2f}")

                        progress_percent = int((total_pl / final_target) * 100) if final_target > 0 else 0
                        st.progress(progress_percent, text=f"‡∏Ñ‡∏ß‡∏≤‡∏°‡∏Ñ‡∏∑‡∏ö‡∏´‡∏ô‡πâ‡∏≤: ${total_pl:,.2f} / ${final_target:,.2f}")
                    else:
                        st.balloons()
                        st.success("üéâ ‡∏¢‡∏¥‡∏ô‡∏î‡∏µ‡∏î‡πâ‡∏ß‡∏¢! ‡∏Ñ‡∏∏‡∏ì‡∏ö‡∏£‡∏£‡∏•‡∏∏‡πÄ‡∏õ‡πâ‡∏≤‡∏´‡∏°‡∏≤‡∏¢‡∏™‡∏∏‡∏î‡∏ó‡πâ‡∏≤‡∏¢‡πÅ‡∏•‡πâ‡∏ß!")
                else:
                    st.info("‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏Å‡∏£‡∏≠‡∏Å‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÉ‡∏ô‡πÄ‡∏°‡∏ô‡∏π‡∏î‡πâ‡∏≤‡∏ô‡∏Ç‡πâ‡∏≤‡∏á‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏Å‡∏≤‡∏£‡∏ß‡∏¥‡πÄ‡∏Ñ‡∏£‡∏≤‡∏∞‡∏´‡πå")
            
            except Exception as e:
                st.error(f"‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì: {e}")